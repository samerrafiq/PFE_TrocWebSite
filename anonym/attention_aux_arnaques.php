<h3>Les différents types d'arnaques</h3>
<ul>
<li><strong>L'objet attrayant qui n'existe pas</strong> : Le dernier portable, la dernière console de jeu ou le dernier modèle d'un véhicule à un prix très attractif ... fuyez ! Ce genres d'annonces est soit destinée à voler des objets (lors de trocs vous envoyez votre objet en premier, puis le troqueur ne donne plus de nouvelles...) soit à voler de l'argent ou un numéro de carte bancaire (pour les ventes).</li>
<li><strong>Achat de votre objet plus cher que ce qu'il ne vaut</strong> : Si une personne vous propose de payer immédiatement votre objet par virement bancaire et même parfois plus cher que sa valeur... fuyez ! De faux ordres de virement vous seront adressés (la qualité de ces faux ordres de virement peut être excellente).</li>
<li><strong>SPAM : arnaques « à l’héritage »</strong> Les escrocs diffusent un message électronique proposant de mirobolantes commissions, en échange de l’utilisation d’un compte bancaire pour effectuer des virements d’un montant très élevé. <br />
  Il s’agit en général de prétendus fonds qui se trouveraient bloqués dans une prétendue banque au nom de prétendus héritiers ne pouvant récupérer leurs avoirs sans l’intervention d’un tiers. <br />
  Les escrocs excellent dans l’art d’instaurer un doute dans l’esprit des personnes qui se font duper en usurpant le nom de personnes réelles et de vraies banques et en créant des adresses électronique évocatrices, mais fausses. <br />
  L’objectif des escrocs consiste soit à obtenir le numéro de votre compte bancaire et un exemplaire de votre signature pour donner de faux ordres de virement à votre banque, soit à vous faire payer de prétendus « frais de dossier » préalables, qui se succèderont alors en cascade. <br />
  Plusieurs variantes circulent sur Internet : personne décédée à la suite d’un assassinat politique ou dans un accident d’avion, mystérieuse valise diplomatique ou malle qui se trouverait bloquée à Abidjan, etc. <br />
  L’magination des escrocs est sans limite.</li>
<li><strong>Arnaques à la Carte Grise</strong> : Méfiez-vous si un acheteur potentiel se propose d'acheter votre véhicule sans discussion et demande une copie de votre carte grise. Vos numéros d'immatriculation pourront servir à maquiller des voitures volées.</li>
